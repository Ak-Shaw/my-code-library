
a = int(input("enter a number: "))
b = int(input("enter another number: "))

print("a = ", a, "and b = ", b)

# floor division
print("(quotient) a // b = ", a // b)

print("(remainder) a % b = ", a % b)

print("(division) a / b = ", a / b)

print("(multiplication) a * b = ", a * b)

print("(power) a ** b = ", a ** b)

print("(sum) a + b = ", a + b)

print("(subtract)  a - b = ", a - b)
